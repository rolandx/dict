       <p class="p_2 color ">Windows Mobile版本安装说明：</p>
       <p class="p_2">1、安装：在资源管理器中找到存储卡的位置并打开，找到程序资料data文件夹，依次打开data-setup-Windows Mobile文件夹，找到fltrpdict.cab文件，点击打开即可进行安装。（提示：安装过程中，手机会提示程序安装的目录，此时选择存储为程序的存储目录即TF卡）</p>
       <p class="p_2">2、使用：在程序中找到“外研社手机词典”，打开即可运行程序。</p>
	   <p class="p_2 color" align="right"><a href="shuoming_wm.php" title="Windows Mobile版本安装说明">>>查看详情</a>&nbsp;&nbsp;&nbsp;&nbsp;</p>
	  <br />
       <p class="p_2 color ">Symbian版本安装说明：</p>
	   <p class="p_2">1、安装：参看手机使用说明书，在手机中找到存储卡的位置并打开，找到程序资料data文件夹，依次打开data-setup-Symbian文件夹，找到fltrpdict_rel.sis文件，点击打开即可进行安装。（注意：程序安装目录选择TF卡）</p>
	   <p class="p_2">2、使用：在程序安装目录中找到“外研社手机词典”，打开即可运行程序。</p> 
	   <p class="p_2 color " align="right"><a href="shuoming_sb.php" title="Symbian版本安装说明">>>查看详情</a>&nbsp;&nbsp;&nbsp;&nbsp;</p>
	  <br />
       <p class="p_2 color ">Java版本安装说明：</p>
		<p class="p_2">1、安装：参看手机使用说明书，在手机中找到存储卡的位置并打开，找到程序资料data文件夹，依次打开data-setup-Java文件夹，找到fltrpdict240.jar和fltrpdict240.jad文件，点击打开其中一个即可进行安装。（注意：程序安装目录选择TF卡）</p>
		<p class="p_2">2、设置：在程序安装目录中找到“外研社手机词典”，点击菜单“选项”或者“设置”，找到“权限”栏打开，将“读取用户数据”和“增加和编辑用户数据”修改为“总是运行”或者“从不询问”。<br>注：每款手机的设置方法不同，可根据具体手机型号进行设置。</p>
		<p class="p_2">3、使用：打开设置好的程序即可运行。</p>
	   <p class="p_2 color " align="right"><a href="shuoming_ja.php" title="Java版本安装说明">>>查看详情</a>&nbsp;&nbsp;&nbsp;&nbsp;</p>
	  
       <p class="p_2 color ">Java版本s40系统安装说明：</p>
		 <br>1、安装：将存储卡放置到手机中，在手机主功能界面，找到应用软件并打开，找到No Name（存储卡）依次打开data-setup-java-Verisign,可以看到“外研社手机词典”应用程序。
                 <br>2、设置：选中“外研社手机词典”，选择“操作”菜单依次进入-应用软件接入-数据接入-读取用户数据-总是允许；返回到“外研社手机词典”，选择“操作”菜单依次进入-应用软件接入-数据接入-编辑和增加用户数据-总是允许。
                 <br>3、运行：返回到“外研社手机词典”，点击打开看到“外研社手机词典”欢迎界面即是打开程序。

                
                
