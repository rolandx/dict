                     <br />
					<p class="p0">Introduction: </p> 
					<p class="p0">Already the largest publisher of foreign language publications in China, Foreign Language Teaching and Research Press (FLTRP) also leads the trend in digital and electronic publishing. In August 2009, FLTRP launched its own Mobile Dictionary series, with a product line covering eight languages, including English, Japanese, German, French, Korean, Spanish, Russian and Italian. With multi-functions of word searching, e-book and dialogue all in one, applicable to multiple platforms on most mobile phone brands and models in market, FLTRP Mobile Dictionary is itself a mini e-dictionary. </p> 
					<p class="p0">From 10:30am to 11:00am, September 4, FLTRP Mobile Dictionary Product Release will be held at Beijing International Book Fair (BIBF). Honored guests attending the event will include members from FLTRP senior management, the publisher’s domestic and overseas partners, as well as friends from industry and online media. A special zone will be set up at the event with FLTRP mobile dictionary products on display. </p> 
