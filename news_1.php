					<p class="p0">活动简介：</p> 
					<p class="p0">作为国内最大的外语出版机构，外语教学与研究出版社一直走在出版内容数字化电子化的前端。2009年8月，外研社隆重推出系列手机词典产品，覆盖八个语种，英语、日语、德语、法语、韩语、西班牙语、俄语、意大利语，具有查词、阅读和会话功能，可适用于多个操作平台、适配市面上大多数的品牌和型号的手机，将手机词典的功能提升至电子词典的水平。 </p>
